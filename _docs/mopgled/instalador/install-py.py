"""Wrapper do instalador MopGled Client (Python)."""
from __future__ import annotations

import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
INSTALLER = ROOT / "modulos" / "mopgled-client" / "instalar.py"

if not INSTALLER.exists():
    print("[ERRO] instalador não encontrado em", INSTALLER)
    sys.exit(1)

cmd = [sys.executable, str(INSTALLER), *sys.argv[1:]]
raise SystemExit(subprocess.call(cmd))

